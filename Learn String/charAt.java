/* charAt proporties use */
//basically charAt proporties find the any index of character
public class charAt {
	public static void main(String[] args) {
		String name = "Hello World"; 
		System.out.println(name.charAt(7)); // use charAt and find any index of character 

		/* use charAt and print string */
		for(int i = 0; i < name.length(); i++) {
			System.out.print(name.charAt(i));
		};
	};
};

